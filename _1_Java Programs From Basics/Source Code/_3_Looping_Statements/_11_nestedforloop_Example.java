package _3_Looping_Statements;

public class _11_nestedforloop_Example
{
    public static void main(String[] args)
    {
        for(int i =0 ;i<=5;i++)
        {
            for(int j = 0; j<=5;j++)
            {
                System.out.println("i = "+i+" and j = "+j);
            }
        }
    }
}
