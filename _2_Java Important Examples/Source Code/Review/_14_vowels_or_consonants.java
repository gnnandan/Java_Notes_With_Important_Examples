package Review;

public class _14_vowels_or_consonants
{
    public static void main(String[] args)
    {
        char ch = 'a';

        if(ch=='a'||ch=='e'||ch=='i'||ch=='o'||ch=='u')
        {
            System.out.println("The character "+ch+" is vowel");
        }
        else
        {
            System.out.println("The character "+ch+" is vowel");
        }
    }
}
