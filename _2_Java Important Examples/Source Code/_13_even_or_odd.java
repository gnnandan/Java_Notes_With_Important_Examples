public class _13_even_or_odd
{
    public static void main(String[] args)
    {
        int num = 1000;

        if(num % 2==0)
        {
            System.out.println("The number "+num+" is even");
        }
        else
        {
            System.out.println("The number "+num+" is odd");
        }
    }
}
